  <h2>Site Structure</h2>
  <p>
    The main bulk of the site is composed of articles.
    Each article contains my thoughts on a granular subject.
    The goal is for each article to be a relatively short, self-contained item of thought that connects to other articles to form larger, more complex ideas.
    Therefore, each article will have a list of articles that it links to.
    This will form a web of interconnected articles that can be traversed like a web.
  </p>
  <p>
    For those of you with a technical background, this is known as a weighted, directed graph.
    It is weighted because weights are used to determine the order in which articles appear on the left sidebar in any given page.
  </p>
  <p>
    To navigate the site, there are two main options:  </p>

    <ol>
      <li>Use the left sidebar to show all articles that the current article is linking to. It also shows every article linking to the current article to that the web of interconnected ideas can be traversed in both directions.</li>
      <li>There is a graph page <a href="/graph">here</a> that can be used to see the graph of the entire site with a bird's eye view.
